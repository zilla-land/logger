/** Representation of the various log levels. */
export enum LogLevel {

    /** A log-level that represents the absence of a log-level. */
    NONE  = -1,

    /** A debug log-level. */
    DEBUG = 0,

    /** An info log-level. */
    INFO  = 1,

    /** A warning log-level. */
    WARN  = 2,

    /** An error log-level. */
    ERROR = 3,

    /** A fatal log-level. */
    FATAL = 4

}
